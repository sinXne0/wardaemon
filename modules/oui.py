def lookup(mac: str) -> str:
    """Return manufacturer name for a MAC, or '' if unknown."""
    if not mac or len(mac) < 8:
        return ""
    prefix = mac.upper().replace(":", "").replace("-", "")[:6]
    return _OUI.get(prefix, "")
